/**
 * Phase 3 — Generate src/data/words.ts from hsk-enriched.json.
 *
 * Input:  scripts/data/hsk-enriched.json
 * Output: src/data/words.ts  (replaces existing file)
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const enrichedPath = path.join(__dirname, "data", "hsk-enriched.json");
const outPath = path.join(__dirname, "..", "src", "data", "words.ts");

const TYPE_MAP = {
  noun: "Type.N",
  verb: "Type.V",
  adjective: "Type.Adj",
  adverb: "Type.Adv",
  pronoun: "Type.Pron",
  question_pronoun: "Type.QPron",
  numeral: "Type.Num",
  measure_word: "Type.MW",
  preposition: "Type.Prep",
  conjunction: "Type.Conj",
  particle: "Type.Part",
  interjection: "Type.Interj",
  auxiliary_verb: "Type.AuxV",
  modal_verb: "Type.ModV",
  time_word: "Type.TimeW",
  location_word: "Type.LocW",
  expression: "Type.Expr",
  proper_noun: "Type.PN",
};

function formatWord(w) {
  const types = (w.types ?? ["noun"])
    .map((t) => TYPE_MAP[t] ?? "Type.N")
    .join(",");
  const sinoVi = w.sino_vi ? `"${w.sino_vi}"` : "null";
  const en = w.en.replace(/"/g, '\\"');
  const vi = (w.vi ?? "").replace(/"/g, '\\"');
  return `  { chinese:"${w.chinese}", pinyin:"${w.pinyin}", en:"${en}", vi:"${vi}", sino_vi:${sinoVi}, types:[${types}], hsk:${w.hsk} },`;
}

function generate() {
  const enriched = JSON.parse(fs.readFileSync(enrichedPath, "utf-8"));

  // enriched is a dict keyed by chinese; sort by hsk then by original order
  const words = Object.values(enriched).sort((a, b) => a.hsk - b.hsk);

  const byLevel = {};
  for (const w of words) {
    (byLevel[w.hsk] = byLevel[w.hsk] ?? []).push(w);
  }

  const lines = [];

  lines.push(`// Auto-generated by scripts/generate-words-ts.mjs — do not edit by hand`);
  lines.push(``);
  lines.push(`enum Type {`);
  lines.push(`  N = "noun",`);
  lines.push(`  V = "verb",`);
  lines.push(`  Adj = "adjective",`);
  lines.push(`  Adv = "adverb",`);
  lines.push(`  Pron = "pronoun",`);
  lines.push(`  QPron = "question_pronoun",`);
  lines.push(`  Num = "numeral",`);
  lines.push(`  MW = "measure_word",`);
  lines.push(`  Prep = "preposition",`);
  lines.push(`  Conj = "conjunction",`);
  lines.push(`  Part = "particle",`);
  lines.push(`  Interj = "interjection",`);
  lines.push(`  AuxV = "auxiliary_verb",`);
  lines.push(`  ModV = "modal_verb",`);
  lines.push(`  TimeW = "time_word",`);
  lines.push(`  LocW = "location_word",`);
  lines.push(`  Expr = "expression",`);
  lines.push(`  PN = "proper_noun",`);
  lines.push(`}`);
  lines.push(``);
  lines.push(`type Word = {`);
  lines.push(`  chinese: string;`);
  lines.push(`  pinyin: string;`);
  lines.push(`  en: string;`);
  lines.push(`  vi: string;`);
  lines.push(`  sino_vi: string | null;`);
  lines.push(`  types: Type[];`);
  lines.push(`  hsk: number;`);
  lines.push(`};`);
  lines.push(``);
  lines.push(`const WORDS: Word[] = [`);

  for (let level = 1; level <= 6; level++) {
    const group = byLevel[level] ?? [];
    lines.push(`  // HSK ${level} (${group.length} words)`);
    for (const w of group) {
      lines.push(formatWord(w));
    }
    lines.push(``);
  }

  lines.push(`];`);
  lines.push(``);
  lines.push(`export { WORDS, type Type, type Word };`);
  lines.push(``);

  fs.writeFileSync(outPath, lines.join("\n"), "utf-8");

  const total = words.length;
  console.log(`Generated ${outPath}`);
  console.log(`Total words: ${total}`);
  for (let l = 1; l <= 6; l++) {
    console.log(`  HSK ${l}: ${(byLevel[l] ?? []).length}`);
  }
}

generate();
