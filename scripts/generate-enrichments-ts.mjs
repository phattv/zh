/**
 * Phase 5 — Generate src/data/enrichments.ts from hsk-learn-enrichment.json.
 *
 * Input:  scripts/data/hsk-learn-enrichment.json
 * Output: src/data/enrichments.ts  (replaces existing file)
 *
 * Run after: node scripts/enrich-learn.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const inPath = path.join(__dirname, "data", "hsk-learn-enrichment.json");
const outPath = path.join(__dirname, "..", "src", "data", "enrichments.ts");

function q(str) {
  return JSON.stringify(str);
}

function formatExample(ex) {
  return `    { zh: ${q(ex.zh)}, pinyin: ${q(ex.pinyin)}, en: ${q(ex.en)} }`;
}

function formatWord(chinese) {
  return `    { chinese: ${q(chinese.chinese)}, pinyin: ${q(chinese.pinyin)}, en: ${q(chinese.en)} }`;
}

function generate() {
  if (!fs.existsSync(inPath)) {
    console.error(`Missing ${inPath} — run enrich-learn.mjs first`);
    process.exit(1);
  }

  const data = JSON.parse(fs.readFileSync(inPath, "utf-8"));
  const entries = Object.entries(data);

  const lines = [];
  lines.push(`// Auto-generated by scripts/generate-enrichments-ts.mjs — do not edit by hand`);
  lines.push(`// Run: node scripts/enrich-learn.mjs && node scripts/generate-enrichments-ts.mjs`);
  lines.push(``);
  lines.push(`export type Enrichment = {`);
  lines.push(`  explanation_zh: string;`);
  lines.push(`  examples: Array<{ zh: string; pinyin: string; en: string }>;`);
  lines.push(`  synonyms: Array<{ chinese: string; pinyin: string; en: string }>;`);
  lines.push(`  antonyms: Array<{ chinese: string; pinyin: string; en: string }>;`);
  lines.push(`};`);
  lines.push(``);
  lines.push(`export const ENRICHMENTS: Record<string, Enrichment> = {`);

  for (const [chinese, enrichment] of entries) {
    const examplesStr = (enrichment.examples ?? []).map(formatExample).join(",\n");
    const synonymsStr = (enrichment.synonyms ?? []).map(formatWord).join(",\n");
    const antonymsStr = (enrichment.antonyms ?? []).map(formatWord).join(",\n");

    lines.push(`  ${q(chinese)}: {`);
    lines.push(`    explanation_zh: ${q(enrichment.explanation_zh ?? "")},`);
    lines.push(`    examples: [`);
    if (examplesStr) lines.push(examplesStr + ",");
    lines.push(`    ],`);
    lines.push(`    synonyms: [`);
    if (synonymsStr) lines.push(synonymsStr + ",");
    lines.push(`    ],`);
    lines.push(`    antonyms: [`);
    if (antonymsStr) lines.push(antonymsStr + ",");
    lines.push(`    ],`);
    lines.push(`  },`);
  }

  lines.push(`};`);
  lines.push(``);

  fs.writeFileSync(outPath, lines.join("\n"), "utf-8");
  console.log(`Generated ${outPath}`);
  console.log(`Total enriched words: ${entries.length}`);
}

generate();
